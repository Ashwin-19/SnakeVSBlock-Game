public class Point
{
    /**
     * X-coordinate
     */
    private int x;

    /**
     * Y-coordinate
     */
    private int y;

    /**
     * To get the X coordinate of the point
     * @return x-ccordinate
     */
    public int getX() {
        return x;
    }

    /**
     * To set the X coordinate of the point
     */
    public void setX(int x) {
        this.x = x;
    }

    /**
     * To get the Y-coordinate of the point
     * @return y-coordinate
     */
    public int getY() {
        return y;
    }

    /**
     * To set the Y-coordinate of the point
     */
    public void setY(int y) {
        this.y = y;
    }
}
